<?php

// perulangan untuk baris
for($i=1; $i <= 5; $i++){
    // perulangan cetak bintang
    for($a=1; $a <= $i; $a++){
        echo "*";
    }
    echo "<br>";
}
echo "<hr>";
// perulangan baris dari 5 ke 1
for($i=5; $i >= 1; $i--){
    // perulangan cetak bintang
    for($a=1; $a <= $i; $a++){
        echo "*";
    }
    echo "<br>";
}
